package com.sdtbu.chapter08;

public abstract class Maintainer
{
    private String name;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    private String uid;
    public abstract void work();
}
